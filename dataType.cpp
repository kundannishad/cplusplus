#include <iostream>;
using namespace std;

int main()
{
    int myNumber = 10;
    float myFloatNumber = 203.133;
    double myDoubleNumber = 234.23;
    char myCharacter = 'K';
    bool myBoolean = true;
    string myText = "Hello World";

    cout << "Integer Data type:" << myNumber << endl;
    cout << "Float data type :" << myFloatNumber << endl;

    cout << "Double data type : " << myDoubleNumber << endl;

    cout << "Char dat type :" << myCharacter << endl;

    cout << "Boolean data type: " << myBoolean << endl;

    cout << " My string data type:" << myText << endl;
}